    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
    CHuesped huesped = new CHuesped();

    huesped.setNombre(txtNombre.getText());
    huesped.setApellido(txtApellido.getText());
    huesped.setDni(txtDni.getText());
    huesped.setTelefono(txtDni.getText());
    huesped.setCorreo(txtCorreo.getText());

    CHuespedDAO dao = new CHuespedDAO();
    dao.guardarHuesped(huesped);
    dao.mostrarHuespedes(tbHuespedes);

    limpiarCampos();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
    CHuesped huesped = new CHuesped();

    huesped.setIdHuesped(Integer.parseInt(txtIdHuesped.getText()));
    huesped.setNombre(txtNombre.getText());
    huesped.setApellido(txtApellido.getText());
    huesped.setDni(txtDni.getText());
    huesped.setTelefono(txtDni.getText());
    huesped.setCorreo(txtCorreo.getText());

    CHuespedDAO dao = new CHuespedDAO();
    dao.modificarHuesped(huesped);
    dao.mostrarHuespedes(tbHuespedes);

    limpiarCampos();
    }//GEN-LAST:event_btnModificarActionPerformed

