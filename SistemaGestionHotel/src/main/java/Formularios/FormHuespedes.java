    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
    int idHuesped = Integer.parseInt(txtIdHuesped.getText());

    CHuespedDAO dao = new CHuespedDAO();
    dao.eliminarHuesped(idHuesped);
    dao.mostrarHuespedes(tbHuespedes);

    limpiarCampos();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tbHuespedesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHuespedesMouseClicked
        // TODO add your handling code here:
    int fila = tbHuespedes.getSelectedRow();

    txtIdHuesped.setText(tbHuespedes.getValueAt(fila, 0).toString());
    txtNombre.setText(tbHuespedes.getValueAt(fila, 1).toString());
    txtApellido.setText(tbHuespedes.getValueAt(fila, 2).toString());
    txtDni.setText(tbHuespedes.getValueAt(fila, 3).toString());
    txtTelefono.setText(tbHuespedes.getValueAt(fila, 4).toString());
    txtCorreo.setText(tbHuespedes.getValueAt(fila, 5).toString());
    }//GEN-LAST:event_tbHuespedesMouseClicked

