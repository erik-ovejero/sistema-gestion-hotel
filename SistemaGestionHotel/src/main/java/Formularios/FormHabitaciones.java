    private void btnGuardarHabitacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarHabitacionActionPerformed
        // TODO add your handling code here:
    CHabitacion habitacion = new CHabitacion();

    habitacion.setNumero(txtNumeroHabitacion.getText());
    habitacion.setTipo(txtTipoHabitacion.getText());
    habitacion.setCapacidad(Integer.parseInt(txtCapacidadHabitacion.getText()));
    habitacion.setPrecio(Double.parseDouble(txtPrecioHabitacion.getText()));
    habitacion.setEstado(cbEstadoHabitacion.getSelectedItem().toString());

    CHabitacionDAO dao = new CHabitacionDAO();
    dao.guardarHabitacion(habitacion);
    dao.mostrarHabitaciones(tbHabitacion);

    limpiarCamposHabitacion();
    }//GEN-LAST:event_btnGuardarHabitacionActionPerformed

    private void btnModificarHabitacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarHabitacionActionPerformed
        // TODO add your handling code here:
        CHabitacion habitacion = new CHabitacion();

        habitacion.setIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));
        habitacion.setNumero(txtNumeroHabitacion.getText());
        habitacion.setTipo(txtTipoHabitacion.getText());
        habitacion.setCapacidad(Integer.parseInt(txtCapacidadHabitacion.getText()));
        habitacion.setPrecio(Double.parseDouble(txtPrecioHabitacion.getText()));
        habitacion.setEstado(cbEstadoHabitacion.getSelectedItem().toString());

        CHabitacionDAO dao = new CHabitacionDAO();
        dao.modificarHabitacion(habitacion);
        dao.mostrarHabitaciones(tbHabitacion);

        limpiarCamposHabitacion();
    }//GEN-LAST:event_btnModificarHabitacionActionPerformed

    private void btnEliminarHabitacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarHabitacionActionPerformed
        // TODO add your handling code here:
        int idHabitacion = Integer.parseInt(txtIdHabitacion.getText());

        CHabitacionDAO dao = new CHabitacionDAO();
        dao.eliminarHabitacion(idHabitacion);
        dao.mostrarHabitaciones(tbHabitacion);

        limpiarCamposHabitacion();
    }//GEN-LAST:event_btnEliminarHabitacionActionPerformed

