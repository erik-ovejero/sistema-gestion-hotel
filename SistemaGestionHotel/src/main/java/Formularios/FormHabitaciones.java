    private void tbHabitacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHabitacionMouseClicked
        // TODO add your handling code here:
        int fila = tbHabitacion.getSelectedRow();

        txtIdHabitacion.setText(tbHabitacion.getValueAt(fila, 0).toString());
        txtNumeroHabitacion.setText(tbHabitacion.getValueAt(fila, 1).toString());
        txtTipoHabitacion.setText(tbHabitacion.getValueAt(fila, 2).toString());
        txtCapacidadHabitacion.setText(tbHabitacion.getValueAt(fila, 3).toString());
        txtPrecioHabitacion.setText(tbHabitacion.getValueAt(fila, 4).toString());
        cbEstadoHabitacion.setSelectedItem(tbHabitacion.getValueAt(fila, 5).toString());
    }//GEN-LAST:event_tbHabitacionMouseClicked

