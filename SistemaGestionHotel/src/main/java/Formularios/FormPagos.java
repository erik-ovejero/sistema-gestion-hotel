    private void btnEliminarPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarPagoActionPerformed
        // TODO add your handling code here:
        try {
            if (txtIdPago.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un pago de la tabla.");
                return;
            }

            int confirmacion = JOptionPane.showConfirmDialog(
                    this,
                    "¿Está seguro de eliminar este pago?",
                    "Confirmar eliminación",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirmacion == JOptionPane.YES_OPTION) {
                CPagoDAO dao = new CPagoDAO();

                dao.eliminarPago(Integer.parseInt(txtIdPago.getText()));
                dao.mostrarPagos(tbPagos);

                limpiarCamposPago();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar pago: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarPagoActionPerformed

