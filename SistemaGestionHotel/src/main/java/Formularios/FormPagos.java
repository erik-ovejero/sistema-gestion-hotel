    private void btnGuardarPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPagoActionPerformed
        // TODO add your handling code here:
        try {
            ItemCombo reserva = (ItemCombo) cbReservaPago.getSelectedItem();

            if (reserva == null) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una reserva.");
                return;
            }

            if (dtFechaPago.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una fecha de pago.");
                return;
            }

            CPago pago = new CPago();

            pago.setIdReserva(reserva.getId());
            pago.setFechaPago(new java.sql.Date(dtFechaPago.getDate().getTime()));
            pago.setMonto(Double.parseDouble(txtMontoPago.getText()));
            pago.setMedioPago(cbMedioPago.getSelectedItem().toString());
            pago.setEstadoPago(cbEstadoPago.getSelectedItem().toString());

            CPagoDAO dao = new CPagoDAO();
            dao.guardarPago(pago);
            dao.mostrarPagos(tbPagos);

            limpiarCamposPago();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El monto debe ser numérico.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar pago: " + e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarPagoActionPerformed

    private void tbPagosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPagosMouseClicked
        // TODO add your handling code here:
        int fila = tbPagos.getSelectedRow();

        txtIdPago.setText(tbPagos.getValueAt(fila, 0).toString());

        int idReserva = Integer.parseInt(tbPagos.getValueAt(fila, 1).toString());
        seleccionarItemComboPorId(cbReservaPago, idReserva);

        dtFechaPago.setDate((java.util.Date) tbPagos.getValueAt(fila, 5));
        txtMontoPago.setText(tbPagos.getValueAt(fila, 6).toString());
        cbMedioPago.setSelectedItem(tbPagos.getValueAt(fila, 7).toString());
        cbEstadoPago.setSelectedItem(tbPagos.getValueAt(fila, 8).toString());
    }//GEN-LAST:event_tbPagosMouseClicked

