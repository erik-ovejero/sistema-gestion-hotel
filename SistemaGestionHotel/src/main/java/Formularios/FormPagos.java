    private void seleccionarItemComboPorId(javax.swing.JComboBox combo, int id) {

        for (int i = 0; i < combo.getItemCount(); i++) {

            ItemCombo item = (ItemCombo) combo.getItemAt(i);

            if (item.getId() == id) {
                combo.setSelectedIndex(i);
                break;
            }
        }
    }

    private void limpiarCamposPago() {
    txtIdPago.setText("");
    txtMontoPago.setText("");
    dtFechaPago.setDate(null);

    if (cbReservaPago.getItemCount() > 0) {
        cbReservaPago.setSelectedIndex(0);
    }

    if (cbMedioPago.getItemCount() > 0) {
        cbMedioPago.setSelectedIndex(0);
    }

    if (cbEstadoPago.getItemCount() > 0) {
        cbEstadoPago.setSelectedIndex(0);
    }
}
