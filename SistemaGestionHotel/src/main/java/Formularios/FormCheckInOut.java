    private void btnRegistrarCheckInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarCheckInActionPerformed
        // TODO add your handling code here:
        try {
            if (idReservaSeleccionada == 0) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una reserva de la tabla.");
                return;
            }

            String estado = txtEstadoReservaCheck.getText().trim();

            if (!estado.equalsIgnoreCase("Reservada")) {
                JOptionPane.showMessageDialog(this,
                        "Solo se puede registrar check-in en reservas con estado Reservada.");
                return;
            }

            CCheckInOutDAO dao = new CCheckInOutDAO();

            dao.registrarCheckIn(
                    idReservaSeleccionada,
                    Integer.parseInt(txtIdHabitacionCheck.getText())
            );

            dao.cargarReservas(cbReservaCheck);
            dao.mostrarReservasCheck(tbReservasCheck);

            limpiarCamposCheck();
            idReservaSeleccionada = 0;

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al registrar check-in: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarCheckInActionPerformed

