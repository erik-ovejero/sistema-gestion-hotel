    private void tbReservasCheckMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbReservasCheckMouseClicked
        // TODO add your handling code here:
        int fila = tbReservasCheck.getSelectedRow();

        if (fila >= 0) {

            idReservaSeleccionada
                    = Integer.parseInt(tbReservasCheck.getValueAt(fila, 0).toString());

            txtIdHabitacionCheck.setText(
                    tbReservasCheck.getValueAt(fila, 1).toString());

            txtHuespedCheck.setText(
                    tbReservasCheck.getValueAt(fila, 2).toString());

            txtFechaCheckIn.setText(
                    tbReservasCheck.getValueAt(fila, 4).toString());

            txtFechaCheckOut.setText(
                    tbReservasCheck.getValueAt(fila, 5).toString());

            txtEstadoReservaCheck.setText(
                    tbReservasCheck.getValueAt(fila, 6).toString());
        }
    }//GEN-LAST:event_tbReservasCheckMouseClicked

    private void cbReservaCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbReservaCheckActionPerformed
        // TODO add your handling code here:
        try {
            if (cbReservaCheck.getSelectedItem() != null) {
                ItemCombo reserva = (ItemCombo) cbReservaCheck.getSelectedItem();

                CCheckInOutDAO dao = new CCheckInOutDAO();
                dao.cargarDatosReserva(
                        reserva.getId(),
                        txtHuespedCheck,
                        txtIdHabitacionCheck,
                        txtFechaCheckIn,
                        txtFechaCheckOut,
                        txtEstadoReservaCheck
                );
            }
        } catch (Exception e) {
            // Evita errores mientras se está cargando el combo.
        }
    }//GEN-LAST:event_cbReservaCheckActionPerformed

