    private void seleccionarItemComboPorId(javax.swing.JComboBox combo, int id) {

        for (int i = 0; i < combo.getItemCount(); i++) {
            ItemCombo item = (ItemCombo) combo.getItemAt(i);

            if (item.getId() == id) {
                combo.setSelectedIndex(i);
                break;
            }
        }
    }

    private void limpiarCamposFactura() {
        txtIdFactura.setText("");
        txtImporteTotal.setText("");
        dtFechaEmision.setDate(null);

        if (cbReservaFactura.getItemCount() > 0) {
            cbReservaFactura.setSelectedIndex(0);
        }

        if (cbTipoFactura.getItemCount() > 0) {
            cbTipoFactura.setSelectedIndex(0);
        }

        if (cbEstadoFactura.getItemCount() > 0) {
            cbEstadoFactura.setSelectedIndex(0);
        }

        btnExportarPdf.setEnabled(false);
    }
