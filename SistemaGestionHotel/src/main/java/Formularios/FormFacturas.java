    private void btnEmitirFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmitirFacturaActionPerformed
        // TODO add your handling code here:
        try {
            ItemCombo reserva = (ItemCombo) cbReservaFactura.getSelectedItem();

            if (reserva == null) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una reserva pagada.");
                return;
            }

            if (dtFechaEmision.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una fecha de emisión.");
                return;
            }

            CFactura factura = new CFactura();

            factura.setIdReserva(reserva.getId());
            factura.setFechaEmision(new java.sql.Date(dtFechaEmision.getDate().getTime()));
            factura.setTipoFactura(cbTipoFactura.getSelectedItem().toString());
            factura.setImporteTotal(Double.parseDouble(txtImporteTotal.getText()));
            factura.setEstadoFactura(cbEstadoFactura.getSelectedItem().toString());

            CFacturaDAO dao = new CFacturaDAO();
            dao.emitirFactura(factura);
            dao.mostrarFacturas(tbFacturas);
            dao.cargarReservasPagadas(cbReservaFactura);

            limpiarCamposFactura();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El importe total debe ser numérico.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al emitir factura: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEmitirFacturaActionPerformed

    private void tbFacturasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbFacturasMouseClicked
        // TODO add your handling code here:
        int fila = tbFacturas.getSelectedRow();

        txtIdFactura.setText(tbFacturas.getValueAt(fila, 0).toString());

        int idReserva = Integer.parseInt(tbFacturas.getValueAt(fila, 1).toString());
        seleccionarItemComboPorId(cbReservaFactura, idReserva);

        dtFechaEmision.setDate((java.util.Date) tbFacturas.getValueAt(fila, 5));
        cbTipoFactura.setSelectedItem(tbFacturas.getValueAt(fila, 6).toString());
        txtImporteTotal.setText(tbFacturas.getValueAt(fila, 7).toString());
        cbEstadoFactura.setSelectedItem(tbFacturas.getValueAt(fila, 8).toString());

        btnExportarPdf.setEnabled(true);
    }//GEN-LAST:event_tbFacturasMouseClicked

    private void btnModificarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarFacturaActionPerformed
        // TODO add your handling code here:
        try {
            if (txtIdFactura.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una factura de la tabla.");
                return;
            }

            ItemCombo reserva = (ItemCombo) cbReservaFactura.getSelectedItem();

            CFactura factura = new CFactura();

            factura.setIdFactura(Integer.parseInt(txtIdFactura.getText()));
            factura.setIdReserva(reserva.getId());
            factura.setFechaEmision(new java.sql.Date(dtFechaEmision.getDate().getTime()));
            factura.setTipoFactura(cbTipoFactura.getSelectedItem().toString());
            factura.setImporteTotal(Double.parseDouble(txtImporteTotal.getText()));
            factura.setEstadoFactura(cbEstadoFactura.getSelectedItem().toString());

            CFacturaDAO dao = new CFacturaDAO();
            dao.modificarFactura(factura);
            dao.mostrarFacturas(tbFacturas);
            dao.cargarReservasPagadas(cbReservaFactura);

            limpiarCamposFactura();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al modificar factura: " + e.getMessage());
        }
    }//GEN-LAST:event_btnModificarFacturaActionPerformed

    private void btnEliminarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarFacturaActionPerformed
        // TODO add your handling code here:
        try {
            if (txtIdFactura.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una factura de la tabla.");
                return;
            }

            int confirmacion = JOptionPane.showConfirmDialog(
                    this,
                    "¿Está seguro de eliminar esta factura?",
                    "Confirmar eliminación",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirmacion == JOptionPane.YES_OPTION) {
                CFacturaDAO dao = new CFacturaDAO();

                dao.eliminarFactura(Integer.parseInt(txtIdFactura.getText()));
                dao.mostrarFacturas(tbFacturas);
                dao.cargarReservasPagadas(cbReservaFactura);

                limpiarCamposFactura();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar factura: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarFacturaActionPerformed

